// this involves addition , subtraction division multiplication etc

let num1 = 5;
let num2 = 10;
let result = num1 + num2; // here we have just added the num1 and num2
console.log(result); // output is 15

// you can also subtract
result = num1 - num2;
console.log(result); // -5
// multiplication
result = num1 * num2;
console.log(result); // 50 is the output

// remainder

result = num1 % num2;
console.log(result); // remainder in this case is 5

//  you cann add two booleans
num1 = true;
num2 = true;
result = num1 + num2;
console.log(result); // this will output 2 since true is a one and false is a 0

num1 = false;
num2 = true;
result = num1 + num2;
console.log(result); // this will output a one

// increment

num1 = 5;
num1 ++ // this increases the value of num1 by one
console.log(num1); // answer is 6

// decrement

num2=6
num2-- // decreases the value by one
console.log(num2);

